package com.mounika.project1;

public class foodEntity {
    int id;
    String food;
    int price;

    public foodEntity(int id, String food, int price) {
        this.id = id;
        this.food = food;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getFood() {
        return food;
    }

    public int getPrice() {
        return price;
    }
}